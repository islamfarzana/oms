     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function show(User $user)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $users = User::findOrfail($id);
        $roles = Role::all();
        return view('admin.users.edit')->with(['users' => $users, 'roles' => $roles]);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $users = User::findOrFail($id);
        $users->employee_id = $request->employee_id;
        $users->email = $request->email;
        if($request->password != null) {
            $users->password = Hash::make($request->password);
        }
        $users->roles()->sync($request->roles); // Add or edit roles for specific user
        $users->update();
        return redirect()->route('admin.users.index');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function destroy(User $user)
    {
        $user->roles()->detach(); //remove all roles for deleted user
        $user->delete();
        return redirect()->route('admin.users.index');
    }
}
